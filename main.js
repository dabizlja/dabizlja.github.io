/* ── Apply language ── */
let currentLang = localStorage.getItem('fa-lang') || 'en';

function applyLang(lang, animate = false) {
    const t = translations[lang];
    if (!t) return;

    const doUpdate = () => {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            if (t[key] !== undefined) el.textContent = t[key];
        });
        document.querySelectorAll('[data-i18n-html]').forEach(el => {
            const key = el.dataset.i18nHtml;
            if (t[key] !== undefined) el.innerHTML = t[key];
        });
        document.documentElement.lang = lang;
    };

    if (animate) {
        document.body.classList.add('lang-switching');
        setTimeout(() => {
            doUpdate();
            document.body.classList.remove('lang-switching');
        }, 200);
    } else {
        doUpdate();
    }

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    currentLang = lang;
    localStorage.setItem('fa-lang', lang);
}

applyLang(currentLang);

document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        if (btn.dataset.lang !== currentLang) applyLang(btn.dataset.lang, true);
    });
});

/* ── Nav scroll ── */
const nav = document.getElementById('nav');
const storySection = document.getElementById('story');

function updateNavScrolledState() {
    if (!nav || !storySection) return;
    const triggerY = storySection.offsetTop - nav.offsetHeight - 8;
    nav.classList.toggle('scrolled', window.scrollY >= triggerY);
}

window.addEventListener('scroll', updateNavScrolledState, { passive: true });
window.addEventListener('resize', updateNavScrolledState);
updateNavScrolledState();

/* ── Hero parallax & entrance ── */
const heroBg = document.getElementById('heroBg');
requestAnimationFrame(() => heroBg.classList.add('loaded'));

let heroBgTransitionDone = false;
window.addEventListener('scroll', () => {
    if (!heroBgTransitionDone) {
        heroBg.style.transition = 'none';
        heroBgTransitionDone = true;
    }
    if (window.scrollY < window.innerHeight) {
        heroBg.style.transform = `translateY(${window.scrollY * 0.28}px)`;
    }
}, { passive: true });

/* ── Scroll reveal ── */
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const delay = entry.target.dataset.delay || 0;
            setTimeout(() => entry.target.classList.add('visible'), delay);
            revealObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.1, rootMargin: '0px 0px -48px 0px' });

document.querySelectorAll('.features-grid .feature-card.reveal').forEach((el, i) => {
    el.dataset.delay = i * 100;
});
document.querySelectorAll('.gallery-grid .gallery-item.reveal').forEach((el, i) => {
    el.dataset.delay = i * 60;
});

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

/* ── Gallery lightbox ── */
const lightbox      = document.getElementById('lightbox');
const lightboxImg   = document.getElementById('lightboxImg');
const lightboxClose = document.getElementById('lightboxClose');
const lightboxPrev  = document.getElementById('lightboxPrev');
const lightboxNext  = document.getElementById('lightboxNext');

const galleryImgs = [...document.querySelectorAll('.gallery-item img.gallery-thumb')];
let currentIndex = 0;

function openLightbox(index) {
    currentIndex = index;
    const img = galleryImgs[currentIndex];
    lightboxImg.src = img.src;
    lightboxImg.alt = img.alt;
    lightbox.classList.add('open');
    document.body.style.overflow = 'hidden';
}

function showPrev() {
    currentIndex = (currentIndex - 1 + galleryImgs.length) % galleryImgs.length;
    const img = galleryImgs[currentIndex];
    lightboxImg.src = img.src;
    lightboxImg.alt = img.alt;
}

function showNext() {
    currentIndex = (currentIndex + 1) % galleryImgs.length;
    const img = galleryImgs[currentIndex];
    lightboxImg.src = img.src;
    lightboxImg.alt = img.alt;
}

document.querySelectorAll('.gallery-item').forEach((item, i) => {
    item.addEventListener('click', () => {
        if (!item.querySelector('img.gallery-thumb')) return;
        openLightbox(i);
    });
});

function closeLightbox() {
    lightbox.classList.remove('open');
    document.body.style.overflow = '';
    lightboxImg.src = '';
}

lightboxClose.addEventListener('click', closeLightbox);
lightboxPrev.addEventListener('click', e => { e.stopPropagation(); showPrev(); });
lightboxNext.addEventListener('click', e => { e.stopPropagation(); showNext(); });
lightbox.addEventListener('click', e => { if (e.target === lightbox) closeLightbox(); });
document.addEventListener('keydown', e => {
    if (!lightbox.classList.contains('open')) return;
    if (e.key === 'Escape')      closeLightbox();
    if (e.key === 'ArrowLeft')   showPrev();
    if (e.key === 'ArrowRight')  showNext();
});
